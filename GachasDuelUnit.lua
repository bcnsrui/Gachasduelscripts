Gacha2={}

--유닛 유틸
function Gacha2.UnitCharacter(c)
	Gacha2.SummonUnit(c)
	Gacha2.BattleCounterUnit(c)
	Gacha2.UnitEff(c)
	Gacha2.SnipeUnit(c)
	Gacha2.SpecialUnit(c)
	Gacha2.HintUnit(c)
end

--등장 스크립트
function Gacha2.SummonUnit(c)
	local e1=Effect.CreateEffect(c)
	e1:SetType(EFFECT_TYPE_FIELD)
	e1:SetCode(EFFECT_SPSUMMON_PROC)
	e1:SetProperty(EFFECT_FLAG_UNCOPYABLE+EFFECT_FLAG_SPSUM_PARAM)
	e1:SetRange(LOCATION_HAND)
	e1:SetTargetRange(POS_FACEUP_ATTACK,0)
	e1:SetCondition(Gacha2.sumcon)
	e1:SetOperation(Gacha2.sumop)
	c:RegisterEffect(e1)
end
function Gacha2.summonfilter(c)
	return c:IsDefensePos()
end
function Gacha2.sumcon(e,c)
   if c==nil then return true end
    local tp=c:GetControler()
    return (Duel.CheckRemoveOverlayCard(tp,1,0,c:GetLevel(),REASON_COST)
	or Duel.IsExistingMatchingCard(Gacha2.summonfilter,tp,LOCATION_EMZONE,0,1,nil))
	and not c:IsCode(11050018)
end
function Gacha2.sumop(e,tp,eg,ep,ev,re,r,rp,c)
	local c=e:GetHandler()
	local ct=Duel.GetMatchingGroup(Card.IsFaceup,tp,LOCATION_EXTRA,0,nil):GetFirst()
	if Duel.IsExistingMatchingCard(Gacha2.summonfilter,tp,LOCATION_EMZONE,0,1,nil) then return end
	local g=Duel.GetOverlayGroup(tp,1,0)
	local ag=g:Select(tp,c:GetLevel(),c:GetLevel(),nil)
	Duel.SendtoGrave(ag,REASON_RULE)
end

--1체방어
function Gacha2.BattleCounterUnit(c)
	local e1=Effect.CreateEffect(c)
	e1:SetType(EFFECT_TYPE_FIELD+EFFECT_TYPE_TRIGGER_O)
	e1:SetCode(EVENT_BATTLE_START)
