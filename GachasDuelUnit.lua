function Gacha2.noeffectcondition(e,tp,eg,ep,ev,re,r,rp)
	return not re:GetHandler():IsCode(11050010) and not re:GetHandler():IsCode(11050012)
end
function Gacha2.noonlyatkfllter(c)
    return c:IsPosition(POS_FACEUP_ATTACK) and not c:IsLocation(LOCATION_EMZONE+LOCATION_FZONE)
	and not c:IsSetCard(0xa01)
end
function Gacha2.noonlyatkcon(e,c)
	local tp=e:GetHandlerPlayer()
    return Duel.GetMatchingGroupCount(Gacha2.noonlyatkfllter,tp,LOCATION_ONFIELD,0,e:GetHandler())==0
	and e:GetHandler():IsSetCard(0xa12)
end
function Gacha2.noonlyatkcon2(e,c)
	local tp=e:GetHandlerPlayer()
    return e:GetHandler():IsSetCard(0xa12) and e:GetHandler():IsSetCard(0xa01)
